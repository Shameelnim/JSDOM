// Class List

// Add
 const Add = () => {
  const div = document.getElementById("mydiv");
  div.classList.add("add")
}

// Remove

const Remove = () => {
  const div = document.getElementById("mydiv");
  div.classList.remove("remove")
}


// Create dark ligth mode

const Toggle = () => {
  document.body.classList.toggle("change")
}
